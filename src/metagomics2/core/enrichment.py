"""Monte Carlo GO × Taxonomy enrichment analysis.

This module implements single-sample enrichment testing for DIA-based
peptide abundance data using empirical null distributions generated by
Monte Carlo randomization.

Two complementary shuffle modes are supported:

- **Taxonomy Shuffle** (aspect-stratified): keep peptide GO assignments
  fixed, randomize peptide taxonomy assignments.  For each GO aspect
  (molecular_function, biological_process, cellular_component), the
  shuffle pool is restricted to peptides that have at least one GO
  term in that aspect.  This prevents spurious enrichment of GO root
  terms caused by cross-aspect annotation-quality differences.
- **GO Shuffle** (aspect-stratified): keep peptide taxonomy assignments
  fixed, independently randomize peptide GO assignments within each GO
  aspect (molecular_function, biological_process, cellular_component).
  Tests whether a taxon is unusually enriched/depleted for a GO term,
  controlling for cross-aspect annotation-quality confounds.

Both modes produce two-sided empirical p-values.  Benjamini-Hochberg
FDR correction is applied to produce q-values.
"""

from __future__ import annotations

import logging
from dataclasses import dataclass, field
from typing import TYPE_CHECKING

import numpy as np
from numpy.typing import NDArray

if TYPE_CHECKING:
    from metagomics2.core.aggregation import AggregationResult, ComboAggregate
    from metagomics2.core.annotation import PeptideAnnotation

logger = logging.getLogger(__name__)

# Small constant for numerical stability in log2 enrichment
DEFAULT_EPS = 1e-10


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------


def compute_enrichment_pvalues(
    annotations: list[PeptideAnnotation],
    aggregation: AggregationResult,
    combos: dict[tuple[int, str], ComboAggregate],
    iterations: int = 1000,
    threads: int = 1,
    eps: float = DEFAULT_EPS,
    seed: int | None = None,
    go_namespaces: dict[str, str] | None = None,
) -> None:
    """Run both shuffle modes and populate p/q-values on *combos* in place.

    Args:
        annotations: Peptide annotations for the sample.
        aggregation: Pre-computed aggregation result.
        combos: Dictionary of (tax_id, go_id) → ComboAggregate.
            Updated **in place** with p-value and q-value fields.
        iterations: Number of Monte Carlo iterations.
        threads: Number of parallel workers (accepted but currently unused).
        eps: Epsilon for numerical stability.
        seed: Optional RNG seed for reproducibility.
        go_namespaces: Mapping from GO term ID to namespace string
            (e.g. ``{"GO:0003674": "molecular_function", ...}``).
            Used for aspect-stratified GO shuffling.  If ``None``,
            GO closures are shuffled as a single atomic unit (legacy
            behaviour, not recommended).
    """
    if not combos:
        return

    # Filter to annotated peptides with both taxonomy and GO assignments
    ann_list = [
        a for a in annotations
        if a.is_annotated and a.taxonomy_nodes and a.go_terms
    ]
    if not ann_list:
        return

    # Build index mappings
    idx = _build_index_maps(ann_list, combos, go_namespaces=go_namespaces)
    if idx.n_pairs == 0:
        return

    aspect_stratified = go_namespaces is not None
    logger.info(
        "Running enrichment: %d peptides, %d taxa, %d GO terms, %d tested pairs, "
        "%d iterations, aspect-stratified=%s",
        idx.n_peptides, idx.n_taxa, idx.n_go, idx.n_pairs, iterations,
        aspect_stratified,
    )

    # --- Taxonomy shuffle (p-value for GO within taxon) ---
    # When aspect-stratified, run a separate shuffle per GO aspect so
    # the shuffle pool for each GO term only includes peptides that
    # have at least one GO term in that aspect.  This prevents root
    # terms from appearing enriched due to cross-aspect annotation
    # quality differences.
    if aspect_stratified:
        pvals_tax = _run_taxonomy_shuffle_per_aspect(
            ann_list, combos, go_namespaces, iterations=iterations,
            eps=eps, seed=seed,
        )
    else:
        pvals_tax = _run_shuffle(
            idx, mode="taxonomy", iterations=iterations,
            eps=eps, seed=seed,
        )

    # --- GO shuffle (p-value for taxon within GO) ---
    pvals_go = _run_shuffle(
        idx, mode="go", iterations=iterations,
        eps=eps, seed=seed,
    )

    # BH correction (across ALL tested pairs, both modes)
    qvals_tax = benjamini_hochberg(pvals_tax)
    qvals_go = benjamini_hochberg(pvals_go)

    # Write back to combos
    for i, key in enumerate(idx.pair_keys):
        combo = combos[key]
        combo.pvalue_go_for_taxon = float(pvals_tax[i])
        combo.pvalue_taxon_for_go = float(pvals_go[i])
        combo.qvalue_go_for_taxon = float(qvals_tax[i])
        combo.qvalue_taxon_for_go = float(qvals_go[i])

    logger.info("Enrichment analysis complete.")


def _run_taxonomy_shuffle_per_aspect(
    ann_list: list[PeptideAnnotation],
    combos: dict[tuple[int, str], ComboAggregate],
    go_namespaces: dict[str, str],
    iterations: int,
    eps: float,
    seed: int | None,
) -> NDArray[np.float64]:
    """Run taxonomy shuffle separately for each GO aspect.

    For each aspect (MF, BP, CC), the shuffle pool is restricted to
    peptides that have at least one GO term in that aspect.  This
    ensures the background proportion ``p_bg`` is computed relative
    to peptides with the relevant annotation, not all annotated
    peptides.  Tested pairs are grouped by the GO term's namespace
    and each group is shuffled independently.

    Returns an array of p-values aligned to the *full* ``pair_keys``
    order from the master ``_IndexMaps`` built on the complete
    ``ann_list``.
    """
    # Build the master index maps to get the canonical pair_keys order
    master_idx = _build_index_maps(ann_list, combos, go_namespaces=go_namespaces)
    n_pairs = master_idx.n_pairs
    pvalues = np.ones(n_pairs, dtype=np.float64)  # default: non-significant

    if n_pairs == 0:
        return pvalues

    # Map each pair key → its index in the master pair_keys list
    pair_key_to_master_idx = {k: i for i, k in enumerate(master_idx.pair_keys)}

    # Group tested pairs by GO term aspect
    aspect_pairs: dict[str, list[tuple[int, str]]] = {}
    for key in master_idx.pair_keys:
        _tax_id, go_id = key
        ns = go_namespaces.get(go_id, "unknown")
        aspect_pairs.setdefault(ns, []).append(key)

    # Group peptides by which aspects they contribute to
    aspect_peptide_sets: dict[str, set[int]] = {}
    for p, ann in enumerate(ann_list):
        for go_id in ann.go_terms:
            ns = go_namespaces.get(go_id, "unknown")
            aspect_peptide_sets.setdefault(ns, set()).add(p)
    # Convert sets to sorted lists
    aspect_peptides: dict[str, list[int]] = {
        ns: sorted(peps) for ns, peps in aspect_peptide_sets.items()
    }

    for ns, pairs_in_aspect in aspect_pairs.items():
        pep_indices = aspect_peptides.get(ns)
        if not pep_indices or len(pep_indices) < 2:
            continue

        # Build restricted annotation list and combos for this aspect
        restricted_anns = [ann_list[p] for p in pep_indices]
        restricted_combos = {k: combos[k] for k in pairs_in_aspect if k in combos}

        if not restricted_combos:
            continue

        # Build index maps on the restricted pool
        restricted_idx = _build_index_maps(restricted_anns, restricted_combos)
        if restricted_idx.n_pairs == 0:
            continue

        logger.info(
            "Taxonomy shuffle for %s: %d peptides, %d pairs",
            ns, restricted_idx.n_peptides, restricted_idx.n_pairs,
        )

        # Run taxonomy shuffle on the restricted pool
        aspect_pvals = _run_shuffle(
            restricted_idx, mode="taxonomy", iterations=iterations,
            eps=eps, seed=seed,
        )

        # Map results back to master pair order
        for j, key in enumerate(restricted_idx.pair_keys):
            master_i = pair_key_to_master_idx.get(key)
            if master_i is not None:
                pvalues[master_i] = aspect_pvals[j]

    return pvalues


def benjamini_hochberg(pvalues: NDArray[np.float64]) -> NDArray[np.float64]:
    """Benjamini-Hochberg FDR correction.

    Args:
        pvalues: 1-D array of raw p-values.

    Returns:
        Array of q-values (adjusted p-values), same length.
    """
    n = len(pvalues)
    if n == 0:
        return np.array([], dtype=np.float64)

    # Sort p-values (ascending) and track original order
    order = np.argsort(pvalues)
    sorted_p = pvalues[order]

    # BH adjustment: q_i = min(p_i * n / rank, 1.0), enforced monotonic
    ranks = np.arange(1, n + 1, dtype=np.float64)
    adjusted = np.minimum(sorted_p * n / ranks, 1.0)

    # Enforce monotonicity (backward cumulative minimum)
    for i in range(n - 2, -1, -1):
        adjusted[i] = min(adjusted[i], adjusted[i + 1])

    # Restore original order
    qvalues = np.empty(n, dtype=np.float64)
    qvalues[order] = adjusted
    return qvalues


