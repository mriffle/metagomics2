# Internal data structures
# ---------------------------------------------------------------------------


@dataclass
class _IndexMaps:
    """Integer-indexed representations for fast Monte Carlo iteration."""

    n_peptides: int
    n_taxa: int
    n_go: int
    n_pairs: int

    # Peptide abundance vector  shape (P,)
    abundance: NDArray[np.float64]

    # Boolean membership matrices  shape (P, T) and (P, G)
    # Stored as dense bool arrays; for very large datasets a sparse
    # representation would be more memory-efficient.
    tax_mem: NDArray[np.bool_]
    go_mem: NDArray[np.bool_]

    # Mapping from internal pair index → original combo key
    pair_keys: list[tuple[int, str]]

    # Pair membership as index arrays for efficient slicing
    pair_tax_idx: NDArray[np.intp]   # shape (n_pairs,) — tax column index for each pair
    pair_go_idx: NDArray[np.intp]    # shape (n_pairs,) — GO column index for each pair

    # Observed statistics for tested pairs
    obs_log2: NDArray[np.float64]    # shape (n_pairs,)

    # Aspect-stratified GO column masks for independent shuffling.
    # List of boolean arrays, each shape (G,), indicating which GO columns
    # belong to each aspect.  None when namespace info is unavailable.
    go_aspect_masks: list[NDArray[np.bool_]] | None = None


def _build_index_maps(
    ann_list: list[PeptideAnnotation],
    combos: dict[tuple[int, str], ComboAggregate],
    go_namespaces: dict[str, str] | None = None,
) -> _IndexMaps:
    """Convert annotations and combos into integer-indexed arrays."""

    # Collect unique taxa and GO terms referenced by the combos
    combo_taxa: set[int] = set()
    combo_go: set[str] = set()
    for tax_id, go_id in combos:
        combo_taxa.add(tax_id)
        combo_go.add(go_id)

    # Also include taxa and GO terms from annotations (needed for proper shuffling)
    for ann in ann_list:
        combo_taxa.update(ann.taxonomy_nodes)
        combo_go.update(ann.go_terms)

    tax_list = sorted(combo_taxa)
    go_list = sorted(combo_go)
    tax_to_idx = {t: i for i, t in enumerate(tax_list)}
    go_to_idx = {g: i for i, g in enumerate(go_list)}

    n_pep = len(ann_list)
    n_tax = len(tax_list)
    n_go = len(go_list)

    abundance = np.array([a.quantity for a in ann_list], dtype=np.float64)
    tax_mem = np.zeros((n_pep, n_tax), dtype=np.bool_)
    go_mem = np.zeros((n_pep, n_go), dtype=np.bool_)

    for p, ann in enumerate(ann_list):
        for t in ann.taxonomy_nodes:
            if t in tax_to_idx:
                tax_mem[p, tax_to_idx[t]] = True
        for g in ann.go_terms:
            if g in go_to_idx:
                go_mem[p, go_to_idx[g]] = True

    # Compute marginal abundances first (needed for pair filtering)
    eps = DEFAULT_EPS
    a_total = float(abundance.sum())
    a_tax = tax_mem.T @ abundance   # (T,)
    a_go = go_mem.T @ abundance     # (G,)

    # Build pair arrays — only for combos that exist and have positive
    # marginal abundances (A_TAX > 0 and A_GO > 0).  Pairs with zero
    # marginals would produce degenerate log2 enrichment values driven
    # entirely by the epsilon constant.
    pair_keys: list[tuple[int, str]] = []
    pair_tax: list[int] = []
    pair_go: list[int] = []

    for key in combos:
        tax_id, go_id = key
        if tax_id in tax_to_idx and go_id in go_to_idx:
            ti = tax_to_idx[tax_id]
            gi = go_to_idx[go_id]
            if a_tax[ti] > 0 and a_go[gi] > 0:
                pair_keys.append(key)
                pair_tax.append(ti)
                pair_go.append(gi)

    pair_tax_idx = np.array(pair_tax, dtype=np.intp)
    pair_go_idx = np.array(pair_go, dtype=np.intp)

    # A_JOINT via weighted membership: diag(abundance) @ tax_mem → (P, T)
    weighted_tax = tax_mem * abundance[:, None]   # broadcast multiply
    joint_matrix = weighted_tax.T @ go_mem         # (T, G)

    # Vectorized observed log2 enrichment for all tested pairs
    joint_vals = joint_matrix[pair_tax_idx, pair_go_idx]  # (n_pairs,)
    a_t_vals = a_tax[pair_tax_idx]                         # (n_pairs,)
    a_g_vals = a_go[pair_go_idx]                           # (n_pairs,)
    p_obs = (joint_vals + eps) / (a_t_vals + eps)
    p_bg = (a_g_vals + eps) / (a_total + eps)
    obs_log2 = np.log2(p_obs / p_bg)

    # Build per-aspect column masks for aspect-stratified GO shuffling
    go_aspect_masks: list[NDArray[np.bool_]] | None = None
    if go_namespaces is not None:
        aspects_seen: dict[str, list[int]] = {}
        for j, go_id in enumerate(go_list):
            ns = go_namespaces.get(go_id, "unknown")
            aspects_seen.setdefault(ns, []).append(j)

        go_aspect_masks = []
        for ns in sorted(aspects_seen):
            mask = np.zeros(n_go, dtype=np.bool_)
            for j in aspects_seen[ns]:
                mask[j] = True
            go_aspect_masks.append(mask)

        logger.info(
            "Aspect-stratified GO shuffle: %d aspects (%s)",
            len(go_aspect_masks),
            ", ".join(f"{ns}: {len(cols)} terms" for ns, cols in sorted(aspects_seen.items())),
        )

    return _IndexMaps(
        n_peptides=n_pep,
        n_taxa=n_tax,
        n_go=n_go,
        n_pairs=len(pair_keys),
        abundance=abundance,
        tax_mem=tax_mem,
        go_mem=go_mem,
        pair_keys=pair_keys,
        pair_tax_idx=pair_tax_idx,
        pair_go_idx=pair_go_idx,
        obs_log2=obs_log2,
        go_aspect_masks=go_aspect_masks,
    )


# ---------------------------------------------------------------------------
# Monte Carlo iteration
# ---------------------------------------------------------------------------


def _run_shuffle(
    idx: _IndexMaps,
    mode: str,
    iterations: int,
    eps: float,
    seed: int | None,
) -> NDArray[np.float64]:
    """Run Monte Carlo shuffle in a single process and return two-sided
    empirical p-values.

    All inner loops are fully vectorized with NumPy fancy indexing —
    there are no Python per-pair ``for`` loops.  The matrix operations
    (``@``) use BLAS internally and are already multi-threaded.

    Args:
        idx: Pre-built index maps.
        mode: ``"taxonomy"`` or ``"go"``.
        iterations: Total number of Monte Carlo iterations.
        eps: Epsilon for numerical stability.
        seed: Optional RNG seed for reproducibility.

    Returns:
        Array of two-sided p-values, one per tested pair.
    """
    rng = np.random.default_rng(seed)
    n_pairs = idx.n_pairs

    # Pre-compute quantities that stay fixed
    abundance = idx.abundance
    a_total = float(abundance.sum())
    pair_tax = idx.pair_tax_idx
    pair_go = idx.pair_go_idx

    if mode == "taxonomy":
        fixed_mem = idx.go_mem
        shuffle_mem = idx.tax_mem.copy()
        a_fixed = fixed_mem.T @ abundance  # A_GO, shape (G,) — stays fixed
    else:
        fixed_mem = idx.tax_mem
        shuffle_mem = idx.go_mem.copy()
        a_fixed = fixed_mem.T @ abundance  # A_TAX, shape (T,) — stays fixed

    # Pre-compute weighted fixed side (only needed for GO shuffle mode)
    if mode == "go":
        weighted_fixed = fixed_mem * abundance[:, None]  # (P, T)

    # Store all null statistics to compute two-sided p-values afterwards
    null_values = np.empty((iterations, n_pairs), dtype=np.float64)

    # For aspect-stratified GO shuffle: pre-extract per-aspect sub-matrices
    aspect_submats: list[NDArray[np.bool_]] | None = None
    if mode == "go" and idx.go_aspect_masks is not None:
        aspect_submats = [shuffle_mem[:, mask] for mask in idx.go_aspect_masks]

    for b in range(iterations):
        if mode == "taxonomy":
            perm = rng.permutation(idx.n_peptides)
            shuffled = shuffle_mem[perm]
            weighted_shuffled = shuffled * abundance[:, None]
            joint_matrix = weighted_shuffled.T @ fixed_mem  # (T, G)
            a_shuffled = shuffled.T @ abundance             # (T,)
            joint_vals = joint_matrix[pair_tax, pair_go]
            denom_vals = a_shuffled[pair_tax]
            bg_vals = a_fixed[pair_go]
        elif aspect_submats is not None:
            # Aspect-stratified GO shuffle: independent permutation per
            # aspect.  Peptides that lack any term in an aspect keep
            # their empty row — they do NOT gain terms from other
            # peptides.  Only peptides that have ≥1 term in the aspect
            # participate in that aspect's shuffle.
            shuffled = np.empty_like(shuffle_mem)
            for aspect_mask, aspect_submat in zip(idx.go_aspect_masks, aspect_submats):
                has_aspect = aspect_submat.any(axis=1)  # (P,) bool
                has_idx = np.where(has_aspect)[0]
                if len(has_idx) < 2:
                    shuffled[:, aspect_mask] = aspect_submat
                    continue
                result = aspect_submat.copy()
                perm = rng.permutation(len(has_idx))
                result[has_idx] = aspect_submat[has_idx[perm]]
                shuffled[:, aspect_mask] = result
            joint_matrix = weighted_fixed.T @ shuffled   # (T, G)
            a_shuffled = shuffled.T @ abundance          # (G,)
            joint_vals = joint_matrix[pair_tax, pair_go]
            denom_vals = a_fixed[pair_tax]
            bg_vals = a_shuffled[pair_go]
        else:
            # Legacy: shuffle entire GO closure as one unit
            perm = rng.permutation(idx.n_peptides)
            shuffled = shuffle_mem[perm]
            joint_matrix = weighted_fixed.T @ shuffled   # (T, G)
            a_shuffled = shuffled.T @ abundance          # (G,)
            joint_vals = joint_matrix[pair_tax, pair_go]
            denom_vals = a_fixed[pair_tax]
            bg_vals = a_shuffled[pair_go]

        p_obs_null = (joint_vals + eps) / (denom_vals + eps)
        p_bg = (bg_vals + eps) / (a_total + eps)
        null_values[b] = np.log2(p_obs_null / p_bg)

    # Two-sided empirical p-values
    mean_null = null_values.mean(axis=0)                         # (n_pairs,)
    obs_dev = np.abs(idx.obs_log2 - mean_null)                   # (n_pairs,)
    null_dev = np.abs(null_values - mean_null[None, :])          # (B, n_pairs)
    count_extreme = np.sum(null_dev >= obs_dev[None, :], axis=0) # (n_pairs,)

    pvalues = (1.0 + count_extreme.astype(np.float64)) / (iterations + 1.0)
    return pvalues
